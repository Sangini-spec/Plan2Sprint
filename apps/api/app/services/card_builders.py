"""
Message card builders for Slack (Block Kit) and Microsoft Teams (HTML/Adaptive Cards).

Each builder function returns:
  - For Slack: { "text": str, "blocks": list } — Block Kit JSON
  - For Teams: { "content": str, "content_type": "html" } — HTML body

Notification types:
  - standup_report: Daily standup report for a developer
  - standup_digest: Team standup digest for PO
  - blocker_alert: Blocker flag raised by a developer
  - sprint_approval: Sprint plan ready for PO approval
  - health_alert: Burnout/workload health alert (PO DM only)
  - sprint_assignment: New sprint task assigned to developer
  - ci_failure: CI/CD pipeline failure alert
  - retro_action: Retrospective action item assigned

IMPORTANT: All interactive buttons include a JSON-encoded `value` field
containing action context (IDs, names, org_id) so the Slack interaction
handler knows what resource to update when the button is clicked.
"""

from __future__ import annotations

import json
from datetime import datetime, timezone
from typing import Any

from ..config import settings

FRONTEND_URL = settings.frontend_url


# ===================================================================
# SLACK BLOCK KIT BUILDERS
# ===================================================================

def slack_standup_report(
    dev_name: str,
    yesterday: list[str],
    today: list[str],
    blockers: list[str],
    sprint_name: str = "",
    *,
    report_id: str = "",
    org_id: str = "",
) -> dict:
    """Daily standup report delivered to developer DM."""
    blocks: list[dict[str, Any]] = [
        {
            "type": "header",
            "text": {"type": "plain_text", "text": "📋 Your Daily Standup Report"},
        },
        {
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": f"Good morning, *{dev_name}*! Here's your auto-generated standup:"
                + (f" _{sprint_name}_" if sprint_name else ""),
            },
        },
        {"type": "divider"},
    ]

    # Yesterday
    if yesterday:
        blocks.append({
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": "*✅ Yesterday:*\n" + "\n".join(f"• {item}" for item in yesterday),
            },
        })

    # Today
    if today:
        blocks.append({
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": "*📌 Today:*\n" + "\n".join(f"• {item}" for item in today),
            },
        })

    # Blockers
    if blockers:
        blocks.append({
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": ":rotating_light: *Blockers:*\n" + "\n".join(f"• {b}" for b in blockers),
            },
        })

    blocks.extend([
        {"type": "divider"},
        {
            "type": "actions",
            "elements": [
                {
                    "type": "button",
                    "text": {"type": "plain_text", "text": "View in Plan2Sprint"},
                    "url": f"{FRONTEND_URL}/dev/standups",
                    "style": "primary",
                },
                {
                    "type": "button",
                    "text": {"type": "plain_text", "text": "🚩 Flag a Blocker"},
                    "action_id": "flag_blocker",
                    "style": "danger",
                    "value": json.dumps({
                        "report_id": report_id,
                        "org_id": org_id,
                    }),
                },
            ],
        },
        {
            "type": "context",
            "elements": [
                {
                    "type": "mrkdwn",
                    "text": f"Auto-generated by Plan2Sprint at {datetime.now(timezone.utc).strftime('%H:%M UTC')}",
                }
            ],
        },
    ])

    return {
        "text": f"Standup Report for {dev_name}",
        "blocks": blocks,
    }


