# SLACK + TEAMS: CI/CD FAILURE ALERT (developer notification)
# ===================================================================

def slack_ci_failure(
    dev_name: str,
    repo_name: str,
    branch: str,
    pr_title: str = "",
    failure_summary: str = "",
    run_url: str = "",
    *,
    org_id: str = "",
) -> dict:
    """CI/CD pipeline failure alert — Slack DM to developer."""
    blocks: list[dict[str, Any]] = [
        {
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": ":x: *CI/CD Pipeline Failed*",
            },
        },
        {
            "type": "section",
            "fields": [
                {"type": "mrkdwn", "text": f"*Repository:*\n{repo_name}"},
                {"type": "mrkdwn", "text": f"*Branch:*\n`{branch}`"},
            ],
        },
    ]

    if pr_title:
        blocks.append({
            "type": "section",
            "text": {"type": "mrkdwn", "text": f"*PR:*\n{pr_title}"},
        })

    if failure_summary:
        blocks.append({
            "type": "section",
            "text": {"type": "mrkdwn", "text": f"*Failure:*\n```{failure_summary}```"},
        })

    action_elements: list[dict[str, Any]] = []
    if run_url:
        action_elements.append({
            "type": "button",
            "text": {"type": "plain_text", "text": "View Build Log"},
            "url": run_url,
            "style": "danger",
        })
    action_elements.append({
        "type": "button",
        "text": {"type": "plain_text", "text": "View My GitHub"},
        "url": f"{FRONTEND_URL}/dev/github",
        "style": "primary",
    })

    blocks.extend([
        {"type": "divider"},
        {"type": "actions", "elements": action_elements},
        {
            "type": "context",
            "elements": [
                {"type": "mrkdwn", "text": f"Detected at {datetime.now(timezone.utc).strftime('%H:%M UTC')} • Plan2Sprint"},
            ],
        },
    ])

    return {
        "text": f"CI/CD failed on {repo_name}/{branch}",
        "blocks": blocks,
    }


