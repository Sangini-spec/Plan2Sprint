def teams_standup_report(
    dev_name: str,
    yesterday: list[str],
    today: list[str],
    blockers: list[str],
    sprint_name: str = "",
) -> dict:
    """Daily standup report for Teams chat."""
    yesterday_html = "".join(f"<li>{item}</li>" for item in yesterday)
