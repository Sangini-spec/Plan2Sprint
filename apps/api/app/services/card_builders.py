def slack_retro_summary(
    sprint_name: str,
    action_items: list[str],
    top_theme: str = "",
    went_well: list[str] | None = None,
    to_improve: list[str] | None = None,
    *,
    retro_id: str = "",
    org_id: str = "",
) -> dict:
    """Sprint retrospective summary pushed to team channel by PO."""
    well_items = went_well or []
    improve_items = to_improve or []

    well_text = "\n".join(f"• {item}" for item in well_items) if well_items else "_None recorded_"
    improve_text = "\n".join(f"• {item}" for item in improve_items) if improve_items else "_None recorded_"
    action_text = "\n".join(f"• {item}" for item in action_items) if action_items else "_No action items_"

    blocks: list[dict[str, Any]] = [
        {
            "type": "header",
            "text": {"type": "plain_text", "text": f"📝 Retrospective Summary — {sprint_name}"},
        },
        {
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": f"*Top Theme:* {top_theme}" if top_theme else "*Retrospective outcomes from your PO:*",
            },
        },
        {"type": "divider"},
        {
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": f"*✅ What went well:*\n{well_text}",
            },
        },
        {
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": f"*🔧 To improve:*\n{improve_text}",
            },
        },
        {
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": f"*📌 Action Items:*\n{action_text}",
            },
        },
        {"type": "divider"},
        {
            "type": "actions",
            "elements": [
                {
                    "type": "button",
                    "text": {"type": "plain_text", "text": "View Full Retro"},
                    "url": f"{FRONTEND_URL}/po/retrospective",
                    "style": "primary",
                },
            ],
        },
        {
            "type": "context",
            "elements": [
                {"type": "mrkdwn", "text": f"Shared at {datetime.now(timezone.utc).strftime('%H:%M UTC')} by your Product Owner • Plan2Sprint"},
            ],
        },
    ]

    return {
        "text": f"Retrospective Summary — {sprint_name}",
        "blocks": blocks,
    }


