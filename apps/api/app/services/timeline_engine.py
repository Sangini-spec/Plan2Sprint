async def _load_project(
    db: AsyncSession, org_id: str, project_id: str
) -> Optional[ImportedProject]:
    q = await db.execute(
        select(ImportedProject).where(
            ImportedProject.id == project_id,
            ImportedProject.organization_id == org_id,
        )
    )
    return q.scalar_one_or_none()


async def _load_phases(
    db: AsyncSession, project_id: str
) -> list[ProjectPhase]:
    q = await db.execute(
        select(ProjectPhase)
        .where(ProjectPhase.project_id == project_id)
        .order_by(ProjectPhase.sort_order, ProjectPhase.created_at)
    )
    return list(q.scalars().all())
