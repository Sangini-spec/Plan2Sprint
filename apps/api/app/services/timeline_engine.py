# Phase distribution (AI / Rebalanced modes)
# ---------------------------------------------------------------------------

def _equal_slice_linear_timeline(
    phases: list["TimelinePhase"],
    target: datetime,
) -> None:
    """Lay out ALL phases as equal, non-overlapping slices of the window
    between the project's origin and ``target``.

    Origin = the earliest non-null ``planned_start`` currently on the
    phases. If nothing is set (brand-new project with no work items yet),
    we anchor to ``target - 7 weeks`` as a sensible default — seven weeks
    is the median Plan2Sprint project so far, and the PO can edit the
    target launch if that's wrong.

    This deliberately discards the per-phase min/max date aggregation. For
    AI / Rebalanced modes the raw aggregation overlaps (parallel work),
    which the PO dashboard reported as "jumbled." Equal slicing gives:

      * strictly sequential phase blocks, left to right
      * the first phase starts at project origin
      * each phase is the same width (1/N of the window)
      * the last phase ends exactly on ``target``

    The "current" phase is conveyed separately via
    ``timeline.current_phase_slug`` (driven by ADO + GitHub signals), so
    losing work-item-accurate dates doesn't hide team progress.
    """
    from datetime import timedelta as _td
    if not phases:
        return

    # Figure out origin.
    starts = [p.planned_start for p in phases if p.planned_start is not None]
    origin = min(starts) if starts else target - _td(weeks=7)
    if origin >= target:
        # Target was edited to earlier than any existing phase start —
        # fall back to a one-day window rather than crashing.
        origin = target - _td(days=1)

    window = (target - origin).total_seconds()
    slice_sec = window / len(phases)
    cursor = origin
    for p in phases:
        p.planned_start = cursor
        cursor = cursor + _td(seconds=slice_sec)
        p.planned_end = cursor
    # Pin the last phase exactly to target so floating-point drift doesn't
    # leave the timeline a millisecond short of the Target Launch KPI.
    phases[-1].planned_end = target


def _linearise_phases(phases: list["TimelinePhase"]) -> None:
    """Push each phase's ``planned_start`` forward so no phase overlaps the
    previous one. Preserves original durations when we can (the work-driven
    length of the phase), otherwise leaves the phase as a zero-length marker
    that ``_distribute_future_phases`` will later redistribute.

    We iterate in ``sort_order`` which the caller guarantees is already the
    natural left-to-right phase order.
    """
    from datetime import timedelta as _td
    prev_end: Optional[datetime] = None
    for p in phases:
        if p.planned_start is None:
            # Nothing to linearise — gap stays as TBD for now. Later stages
            # (AI/Rebalanced distribution) will fill it in.
            prev_end = p.planned_end or prev_end
            continue

        # Is this phase overlapping the tail of the previous one?
        if prev_end is not None and p.planned_start < prev_end:
            # Hold on to the original duration so we preserve the phase's
            # weight (bigger phase = longer bar) after the shift.
            original_duration: Optional[_td] = None
            if p.planned_end is not None and p.planned_end > p.planned_start:
                original_duration = p.planned_end - p.planned_start
            p.planned_start = prev_end
            if original_duration is not None:
                p.planned_end = p.planned_start + original_duration
            elif p.planned_end is None or p.planned_end < p.planned_start:
                # No usable duration — collapse to a zero-length marker at the
                # pushed start. The downstream distributor will expand it.
                p.planned_end = p.planned_start
        prev_end = p.planned_end or prev_end


def _distribute_future_phases(
    phases: list["TimelinePhase"],
    target: datetime,
) -> None:
    """Spread every not-yet-done phase across (now → target) in place.

    Mutates ``phases`` so callers (and ``_persist_phase_dates``) see the new
    boundaries. Past phases — defined as ``planned_end`` strictly in the
    past — stay immutable. The final phase (Ready) is pinned to ``target``
    so the hero banner's target-launch label matches the timeline tile
    exactly.

    Distribution logic:
      - Phases with usable original durations (planned_start < planned_end)
        keep their relative weights → proportional redistribution.
      - Fallback: equal share across future phases when original durations
        aren't available (e.g. a Raw project that just got a plan approved
        and had all future phases at TBD).
    """
    from datetime import timedelta as _td  # local to avoid top-level churn
    now = datetime.now(timezone.utc)
    window = (target - now).total_seconds()
    if window <= 0:
        return

    # Partition phases into past (immutable) and future (rescale targets).
    future_idx = [
        i for i, p in enumerate(phases)
        if p.planned_end is None or p.planned_end > now
    ]
    if not future_idx:
        return

    # Compute each future phase's original duration (if any) for proportional
    # weighting. A phase with missing/zero duration contributes nothing and
    # picks up an equal share instead.
    durations: list[float] = []
    for i in future_idx:
        p = phases[i]
        if p.planned_start and p.planned_end and p.planned_end > p.planned_start:
            durations.append((p.planned_end - p.planned_start).total_seconds())
        else:
            durations.append(0.0)
    total_weight = sum(durations)

    cursor = now
    if total_weight <= 0:
        # Equal share
        slice_sec = window / len(future_idx)
        for i in future_idx:
            phases[i].planned_start = cursor
            cursor = cursor + _td(seconds=slice_sec)
            phases[i].planned_end = cursor
    else:
        # Proportional share
        for i, dur in zip(future_idx, durations):
            frac = (dur / total_weight) if total_weight > 0 else 1.0 / len(future_idx)
            slice_sec = window * frac
            phases[i].planned_start = cursor
            cursor = cursor + _td(seconds=slice_sec)
            phases[i].planned_end = cursor

    # Pin the LAST future phase to the target so FP drift doesn't leave us
    # a millisecond early / late — the hero banner label must match.
    phases[future_idx[-1]].planned_end = target


# ---------------------------------------------------------------------------
# Persistence — write computed dates back to project_phases
# ---------------------------------------------------------------------------

async def _persist_phase_dates(
    db: AsyncSession,
    phases: list[TimelinePhase],
) -> None:
    """Push computed planned_start / planned_end onto the stored phase rows.

    Writes one row at a time rather than a bulk UPDATE so we can handle the
    "dates unchanged" short-circuit without running a query per phase.
    """
    if not phases:
        return
    now = datetime.now(timezone.utc)
    touched = False
    for p in phases:
        q = await db.execute(
            select(ProjectPhase).where(ProjectPhase.id == p.id)
        )
        row = q.scalar_one_or_none()
        if row is None:
            continue
        if row.planned_start == p.planned_start and row.planned_end == p.planned_end:
            continue  # unchanged, skip the UPDATE
        row.planned_start = p.planned_start
        row.planned_end = p.planned_end
        row.updated_at = now
        touched = True
    if touched:
        await db.commit()


