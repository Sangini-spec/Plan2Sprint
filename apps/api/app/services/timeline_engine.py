# Raw mode
# ---------------------------------------------------------------------------

async def _compute_raw_timeline(
    db: AsyncSession,
    project: ImportedProject,
) -> TimelineDTO:
    """Derive phase dates from ADO/Jira work-item plan dates.

    For each phase (including the final Ready phase):
      planned_start = MIN(work_item.planned_start) across items assigned to
                      the phase (either via phase_id or via their parent
                      feature's phase_id).
      planned_end   = MAX(work_item.planned_end) across the same set.

    If a phase has no work items with dates, its planned_start/planned_end
    remain None — the UI renders the tile with a "TBD" label.

    The Ready phase, by convention, receives the same end date as Deployment
    & Launch (the product is "ready for customers" when launch completes).
    This keeps the tile visible in Raw mode even when no work item has
    "ready" in its title. If Deployment & Launch itself has no end date, so
    does Ready — and Target Launch shows TBD.
    """
    phases = await _load_phases(db, project.id)

    # Build phase-id -> aggregated dates from work items.
    date_q = await db.execute(
        select(
            WorkItem.phase_id,
            func.min(WorkItem.planned_start).label("min_start"),
            func.max(WorkItem.planned_end).label("max_end"),
        )
        .where(
            WorkItem.organization_id == project.organization_id,
            WorkItem.imported_project_id == project.id,
            WorkItem.phase_id.isnot(None),
        )
        .group_by(WorkItem.phase_id)
    )
    per_phase: dict[str, tuple[Optional[datetime], Optional[datetime]]] = {
        row.phase_id: (row.min_start, row.max_end)
        for row in date_q.all()
    }

    timeline_phases: list[TimelinePhase] = []

    for p in phases:
        start, end = per_phase.get(p.id, (None, None))
        # Special handling for Ready: it trails the last non-Ready phase
        # with a planned_end (Hotfix 17 generalised — used to be
        # "deployment-launch" specifically, but cloud-native order may
        # put UAT after Deployment). We fill in its dates after the main
        # loop once we know which phase is last.
        if p.slug == "ready":
            timeline_phases.append(
                TimelinePhase(
                    id=p.id, slug=p.slug, name=p.name, sort_order=p.sort_order,
                    color=p.color, planned_start=None, planned_end=None,
                )
            )
            continue

        timeline_phases.append(
            TimelinePhase(
                id=p.id, slug=p.slug, name=p.name, sort_order=p.sort_order,
                color=p.color, planned_start=start, planned_end=end,
            )
        )

    # Fix up the Ready phase. Hotfix 17 — used to hardcode anchor on
    # ``deployment-launch``, which broke when phases were reordered to
    # cloud-native (UAT now comes after Deployment). Now we anchor Ready
    # on the last non-Ready phase that has an end date — robust to any
    # reordering or to UAT being the last phase with work.
    last_real_end = None
    for tp in sorted(timeline_phases, key=lambda x: x.sort_order):
        if tp.slug == "ready":
            continue
        if tp.planned_end is not None:
            last_real_end = tp.planned_end
    for tp in timeline_phases:
        if tp.slug == "ready":
            tp.planned_start = last_real_end
            tp.planned_end = last_real_end
            break

    # Linearise so the timeline reads left-to-right without overlaps. Work
    # items from different phases are often worked in parallel (e.g. Core
    # Development running alongside Discovery), which produces overlapping
    # min/max date ranges. For dashboard display the phases must appear as
    # strictly sequential blocks — otherwise the timeline looks "jumbled":
    # Mar-Apr overlaps for four phases in a row. We preserve each phase's
    # work-driven *duration* but push its start to the previous phase's end
    # whenever they overlap.
    _linearise_phases(timeline_phases)

    # Target launch date: respect a MANUAL override if the PO set one, else
    # fall back to the project-level target (set when AI plan is approved, in
    # Sprint 3). In pure Raw mode this is typically None → "TBD" in the UI.
    target = project.target_launch_date
    target_source = project.target_launch_source
    if target is None and last_real_end is not None:
        # Even in Raw mode, if ADO/Jira has given us an end date for the
        # last work phase (whatever its slug — generalised in Hotfix 17),
        # that date *is* the effective launch target until the PO
        # overrides or an AI plan lands. We surface it but mark as AUTO
        # so the UI knows it wasn't hand-picked.
        target = last_real_end
        target_source = target_source or "AUTO"

    return TimelineDTO(
        mode=MODE_RAW,
        phases=timeline_phases,
        target_launch_date=target,
        target_launch_source=target_source,
        target_violated=False,
        target_violated_days=0,
    )


# ---------------------------------------------------------------------------
# AI-Optimized & Rebalanced modes
# ---------------------------------------------------------------------------

async def _compute_ai_timeline(
    db: AsyncSession,
    project: ImportedProject,
) -> TimelineDTO:
    """Timeline driven by the approved SprintPlan.

    Design note: the AI sprint generator already writes ``planned_start`` and
    ``planned_end`` onto each work item as part of plan generation. That means
    the per-phase min/max aggregation we do for Raw mode already produces the
    AI-plan-derived dates — we don't have to duplicate sprint-allocation math
    here. What AI mode adds on top is:

      (1) auto-setting ``ImportedProject.target_launch_date`` from the plan's
          ``estimated_end_date`` (unless the PO manually overrode it)
      (2) enforcing the target as a constraint: if any phase ends after the
          target, we flag ``target_violated`` + the number of days by which
          the plan overshoots (we don't auto-shift — the PO either rebalances
          or hand-moves the target)
      (3) anchoring the Ready phase end to ``target_launch_date`` so the
          final handoff tile reflects the committed launch date, not just
          the last deploy date
    """
    plan = await _load_active_plan(db, project.id)

    timeline = await _compute_raw_timeline(db, project)
    timeline.mode = MODE_AI_OPTIMIZED

    # ---- target_launch_date policy ----
    # The source of truth for target_launch_date is whatever the approve
    # handler or the PATCH /target-launch endpoint last wrote. This function
    # no longer *writes* it — it only reads. That means:
    #   * AUTO: set by the most recent plan approval (sprints.py PATCH handler)
    #     or by a rebalance acceptance. We use the stored value as-is.
    #   * MANUAL: set by the PO via the hover-edit. Stored until the PO
    #     approves another plan (the approve handler resets it).
    # If target_launch_date is unset but we have an active plan, backfill on
    # the fly from the plan — this handles projects whose plans were
    # approved before Sprint 3 (or this refactor) shipped.
    if project.target_launch_date is None and plan is not None and plan.estimated_end_date is not None:
        project.target_launch_date = plan.estimated_end_date
        project.target_launch_source = "AUTO"
        await db.commit()

    timeline.target_launch_date = project.target_launch_date
    timeline.target_launch_source = project.target_launch_source

    # ---- AI-mode phase distribution ----
    # In AI / Rebalanced mode the Raw per-phase min/max aggregation produces
    # a "jumbled" timeline: Discovery, Core Dev, Integration, Testing etc.
    # all overlap in Mar-Apr because the team worked them in parallel. The
    # PO dashboard wants a strictly left-to-right display where each phase
    # starts after the previous one ends and the last phase lands exactly
    # on Target Launch.
    #
    # Simpler and more predictable: IGNORE the raw work-item dates and lay
    # out all 7 phases as equal slices of the full project window, from the
    # earliest work-item start (project origin) through target_launch_date.
    # The "current" phase indicator (from ADO/GitHub signals) still tells
    # the PO where the team actually is — the timeline is just a clean
    # linear backdrop.
    if timeline.target_launch_date is not None:
        _equal_slice_linear_timeline(timeline.phases, timeline.target_launch_date)

    # Anchor the Ready phase to the committed launch date.
    if timeline.target_launch_date is not None:
        for p in timeline.phases:
            if p.slug == "ready":
                p.planned_end = timeline.target_launch_date
                if p.planned_start is None or p.planned_start > timeline.target_launch_date:
                    p.planned_start = timeline.target_launch_date
                break

    # Flag violations: any phase that ends past the target.
    target = timeline.target_launch_date
    if target is not None:
        overruns = [
            (p.planned_end - target).days
            for p in timeline.phases
            if p.slug != "ready" and p.planned_end is not None and p.planned_end > target
        ]
        if overruns:
            timeline.target_violated = True
            timeline.target_violated_days = max(overruns)

    return timeline


async def _compute_rebalanced_timeline(
    db: AsyncSession,
    project: ImportedProject,
) -> TimelineDTO:
    """Timeline driven by a rebalanced SprintPlan.

    Functionally identical to AI-Optimized today — the rebalanced plan has
    already been committed with a new ``estimated_end_date`` at acceptance
    time, and target_launch_date was refreshed against it then. This function
    exists as its own branch so that if we later need rebalance-specific
    behaviour (e.g. "before/after" comparison on the hero banner) it has an
    obvious hook point.
    """
    timeline = await _compute_ai_timeline(db, project)
    timeline.mode = MODE_REBALANCED
    return timeline


