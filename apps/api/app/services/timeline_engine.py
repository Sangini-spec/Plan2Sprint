"""Timeline engine — the single source of truth for the PO dashboard timeline.

A project's timeline can be generated by one of three modes, selected via
``determine_mode``:

    RAW          — no approved AI plan. Dates derived straight from ADO/Jira
                   feature ``planned_start``/``planned_end`` values, grouped
                   into phases by the existing phase-assignment engine.
    AI_OPTIMIZED — an AI SprintPlan has been approved. Dates derived from the
                   plan's sprint allocations. Phases are constrained: no phase
                   may end after ``ImportedProject.target_launch_date``.
                   (Implemented in Sprint 3.)
    REBALANCED   — the active plan is a rebalance of a prior AI plan. Same as
                   AI_OPTIMIZED but the target_launch_date itself may have been
                   pushed forward by acceptance of the rebalance. (Sprint 3.)

Layer 2 ("real-time adjustment") — picking the *current* phase from live ADO
state and GitHub activity — is added in Sprint 5.

Sprint 1 delivered the data model (Ready phase + target_launch_date +
planned_start/planned_end columns). This module (Sprint 2) delivers the Raw
mode. Sprints 3-5 extend it with AI/Rebalanced modes and the real-time layer.
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field
from datetime import datetime, timezone
from typing import Optional

from sqlalchemy import case, select, func, or_, and_
from sqlalchemy.ext.asyncio import AsyncSession
from sqlalchemy.orm import aliased

from ..models.imported_project import ImportedProject
from ..models.project_phase import ProjectPhase
from ..models.work_item import WorkItem

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Public types
# ---------------------------------------------------------------------------

TimelineMode = str  # "RAW" | "AI_OPTIMIZED" | "REBALANCED"

MODE_RAW = "RAW"
MODE_AI_OPTIMIZED = "AI_OPTIMIZED"
MODE_REBALANCED = "REBALANCED"


@dataclass
class TimelinePhase:
    """Serialisable snapshot of a single phase on the timeline."""
    id: str
    slug: str
    name: str
    sort_order: int
    color: str
    planned_start: Optional[datetime]
    planned_end: Optional[datetime]


@dataclass
class TimelineDTO:
    """What ``compute_timeline`` returns — everything the PO dashboard needs
    to render the hero banner timeline without doing date math of its own."""
    mode: TimelineMode
    phases: list[TimelinePhase] = field(default_factory=list)
    # The enforceable deadline the plan is measured against. May be None in
    # pure Raw mode if ADO/Jira has no end dates yet — UI renders "TBD".
    target_launch_date: Optional[datetime] = None
    # "AUTO" | "MANUAL" | None. MANUAL means the PO hand-edited the target.
    target_launch_source: Optional[str] = None
    # True when at least one phase end is beyond target_launch_date. Only
    # meaningful in AI/Rebalanced modes (Raw has no enforced target).
    target_violated: bool = False
    target_violated_days: int = 0

    # ---- Real-time adjustment (Sprint 5) ----
    # The phase the project is CURRENTLY in, according to live signals.
    # In Raw mode this comes from ADO/Jira only. In AI/Rebalanced modes we
    # also check GitHub commit activity and take whichever source shows the
    # most advanced phase — GitHub reflects actual dev progress while ADO
    # cards often lag.
    current_phase_slug: Optional[str] = None
    # "ADO" | "GITHUB" | "BOTH" | None. Tells the UI which signal produced
    # the current-phase decision so it can tooltip the reason.
    current_phase_source: Optional[str] = None
    # Hotfix 16 — phases EARLIER than ``current_phase_slug`` that still have
    # in-flight work. The UI renders these in a distinct "also active"
    # colour (cyan glow) alongside the orange "current" indicator,
    # answering "we've reached Deployment, but Testing still has loose
    # ends" without conflating the two states.
    also_active_phase_slugs: list[str] = field(default_factory=list)


# ---------------------------------------------------------------------------
# Mode detection
# ---------------------------------------------------------------------------

async def determine_mode(
    db: AsyncSession,
    project: ImportedProject,
) -> TimelineMode:
    """Pick RAW / AI_OPTIMIZED / REBALANCED for this project.

    Rules (in order):
      1. If the project has an APPROVED / SYNCED SprintPlan marked
         ``is_rebalanced=True``  → REBALANCED.
      2. Else if it has an APPROVED / SYNCED SprintPlan  → AI_OPTIMIZED.
      3. Else                                            → RAW.

    Sprint 2 returns RAW for everything that isn't obviously in AI/Rebalanced
    modes — full wiring for the other two modes lands in Sprint 3, but the
    detection is already correct so callers can switch on the result now.
    """
    # Delayed import to avoid a circular dep during module load.
    try:
        from ..models.sprint_plan import SprintPlan  # type: ignore
    except Exception:  # noqa: BLE001 - tolerant import
        return MODE_RAW

    accepted_states = ("APPROVED", "SYNCED", "SYNCED_PARTIAL")
    q = await db.execute(
        select(SprintPlan)
        .where(
            SprintPlan.project_id == project.id,
            SprintPlan.status.in_(accepted_states),
        )
        .order_by(SprintPlan.created_at.desc())
    )
    plan = q.scalars().first()
    if plan is None:
        return MODE_RAW
    if getattr(plan, "is_rebalanced", False):
        return MODE_REBALANCED
    return MODE_AI_OPTIMIZED


# ---------------------------------------------------------------------------
# Public entry point
# ---------------------------------------------------------------------------

async def compute_timeline(
    db: AsyncSession,
    org_id: str,
    project_id: str,
    persist: bool = True,
) -> TimelineDTO:
    """Compute the timeline for a project and optionally persist phase dates.

    ``persist=True`` (default) writes the computed ``planned_start`` /
    ``planned_end`` back onto the ``project_phases`` rows so subsequent reads
    don't have to recompute. Callers that just want a read-only peek can pass
    ``persist=False``.
    """
    project = await _load_project(db, org_id, project_id)
    if project is None:
        return TimelineDTO(mode=MODE_RAW)

    mode = await determine_mode(db, project)

    if mode == MODE_RAW:
        timeline = await _compute_raw_timeline(db, project)
    elif mode == MODE_AI_OPTIMIZED:
        timeline = await _compute_ai_timeline(db, project)
    elif mode == MODE_REBALANCED:
        timeline = await _compute_rebalanced_timeline(db, project)
    else:
        # Unreachable under the current determine_mode(), but keeps us safe
        # against someone adding a new mode without wiring it up.
        logger.warning("timeline_engine: unknown mode=%s — falling back to RAW", mode)
        timeline = await _compute_raw_timeline(db, project)
        timeline.mode = mode

    # Layer 2 — real-time adjustment. Pick the "current" phase based on live
    # ADO and GitHub signals. In Raw mode we ignore GitHub by design.
    await _apply_realtime_adjustment(db, project, timeline)

    if persist:
        await _persist_phase_dates(db, timeline.phases)

    return timeline


# ---------------------------------------------------------------------------
# Layer 2 — real-time adjustment
# ---------------------------------------------------------------------------

async def _apply_realtime_adjustment(
    db: AsyncSession,
    project: ImportedProject,
    timeline: TimelineDTO,
) -> None:
    """Set ``timeline.current_phase_slug`` + ``current_phase_source``.

    Logic:
      - ado_slug = phase with the highest sort_order that has ≥1 work item
        NOT in a backlog/new/todo status. This is the "most advanced" phase
        ADO/Jira says we've reached.
      - gh_slug  = output of commit_matcher.infer_github_phase() — the most
        advanced phase inferred from recent commit messages.
      - RAW mode:          current = ado_slug
      - AI/REBALANCED:     current = max(ado_slug, gh_slug) by sort_order
      - source             = "ADO" / "GITHUB" / "BOTH" / None

    Why favour GitHub when it's ahead? Because commits prove work actually
    landed. ADO cards often lag because no one bothered to drag them across.
    """
    ado_slug, ado_also_active = await _infer_ado_phase(db, project)
    gh_slug: Optional[str] = None
    if timeline.mode != MODE_RAW:
        try:
            from .commit_matcher import infer_github_phase  # local import
            gh_slug = await infer_github_phase(
                db, project.organization_id, project.id
            )
        except Exception as e:  # noqa: BLE001 - don't let GH failures blow up the timeline
            logger.warning("commit_matcher failed: %s", e)
            gh_slug = None

    # Build a sort_order lookup from the phases we already loaded.
    sort_by_slug: dict[str, int] = {p.slug: p.sort_order for p in timeline.phases}

    candidates: list[tuple[str, str]] = []  # (source, slug)
    if ado_slug is not None:
        candidates.append(("ADO", ado_slug))
    if gh_slug is not None:
        candidates.append(("GITHUB", gh_slug))

    if not candidates:
        timeline.current_phase_slug = None
        timeline.current_phase_source = None
        timeline.also_active_phase_slugs = []
        return

    # Pick winner by highest sort_order; if tied, use BOTH.
    candidates.sort(key=lambda c: sort_by_slug.get(c[1], -1), reverse=True)
    top_order = sort_by_slug.get(candidates[0][1], -1)
    top = [c for c in candidates if sort_by_slug.get(c[1], -1) == top_order]

    if len(top) == 1:
        source, slug = top[0]
        timeline.current_phase_slug = slug
        timeline.current_phase_source = source
    else:
        # Both sources agree on the same phase
        timeline.current_phase_slug = top[0][1]
        timeline.current_phase_source = "BOTH"

    # Hotfix 16 — populate "also active" loose-ends. We only have
    # ADO-side detail for in-flight stragglers (commit_matcher gives a
    # single phase, not a list). If GitHub picked a more advanced phase
    # than ADO, recompute relative to GH's pick: any phase between ADO's
    # pick and GH's pick that ADO still has in-flight should be flagged.
    final_current_order = sort_by_slug.get(timeline.current_phase_slug or "", -1)
    timeline.also_active_phase_slugs = [
        slug
        for slug in ado_also_active
        if sort_by_slug.get(slug, -1) < final_current_order
    ]
    # If ADO was the source and ado_slug == final, ado_also_active is
    # already correct. If GH is ahead, we additionally flag the
    # ADO-current phase itself as "also active" — work IS happening
    # there, even if GitHub commits have already moved past it.
    if (
        ado_slug
        and timeline.current_phase_slug != ado_slug
        and sort_by_slug.get(ado_slug, -1) < final_current_order
        and ado_slug not in timeline.also_active_phase_slugs
    ):
        timeline.also_active_phase_slugs.append(ado_slug)


async def _infer_ado_phase(
    db: AsyncSession,
    project: ImportedProject,
) -> tuple[Optional[str], list[str]]:
    """Figure out which phase ADO/Jira says the project has reached, plus
    any earlier phases that are still actively being worked on.

    Hotfix 16 — corrected definition of "current phase".
    -----------------------------------------------------
    The previous logic ran two passes: in-flight first, finished as
    fallback. That meant a project with Testing in-flight + Deployment
    DONE would report Testing as current — ignoring the fact that the
    project's frontier has clearly advanced past Testing.

    The new rule: **current = the most-advanced phase that has been
    touched at all** (in-flight OR finished). Touching a phase is proof
    the project reached it. Stragglers in earlier phases are tracked
    separately as ``also_active_phase_slugs`` so the UI can render
    "we're in Deployment, and Testing still has loose ends" with two
    distinct visual indicators.

    Returns ``(current_slug, also_active_slugs)``:
      * ``current_slug`` — phase with the highest ``sort_order`` that has
        ≥1 item in any non-not-started status.
      * ``also_active_slugs`` — earlier phases (sort_order < current) that
        still have ≥1 item in IN_PROGRESS / IN_REVIEW / etc. (NOT
        finished). These are the "loose ends".

    Excluded statuses for "touched":
      - not_started: BACKLOG, NEW, TODO, PROPOSED
    Excluded for "loose end":
      - not_started + finished (DONE, CLOSED, RESOLVED, REMOVED, ABANDONED)
    """
    not_started = {"BACKLOG", "NEW", "TODO", "PROPOSED"}
    finished = {"DONE", "CLOSED", "RESOLVED", "REMOVED", "ABANDONED"}

    # Hotfix 81 (revised in Hotfix 87) — feature-status-aware phase inference
    # with descendant fallback.
    # ----------------------------------------------------------------------
    # The phase classifier only tags TOP-LEVEL features (epics) with
    # ``phase_id``; stories underneath inherit through their parent. Two
    # very different team workflows show up in the wild:
    #
    #   A) Teams that DO advance their feature cards in ADO/Jira
    #      (Plan2Sprint itself, Breakrails). When the PO marks a
    #      feature DONE, the phase should turn GREEN regardless of any
    #      child stories that linger — the team has explicitly signed
    #      off.
    #   B) Teams that DON'T touch the feature card and only drag
    #      stories (Sellixis, MediCare). The feature stays in TODO/
    #      PROPOSED forever even though the underlying work is
    #      progressing; without a fallback the timeline reads every
    #      phase as untouched.
    #
    # Hotfix 81 added an unconditional OR with the child-state — that
    # fixed (B) but broke (A): a DONE feature with one IN_PROGRESS
    # leftover child story made the whole phase show as "loose ends"
    # (cyan), even though the team had clearly closed it out.
    #
    # The corrected rule below is feature-status-aware:
    #
    #   * Feature is FINISHED       (DONE/CLOSED/RESOLVED/REMOVED/ABANDONED)
    #     → phase complete. Children ignored.
    #   * Feature is IN-FLIGHT      (anything past TODO that isn't finished)
    #     → phase touched + in-flight. Children ignored.
    #   * Feature is NOT-STARTED    (TODO/BACKLOG/NEW/PROPOSED)
    #     → fall back to children:
    #         touched   = any child past TODO
    #         in-flight = any child past TODO and not finished
    #
    # This keeps both workflows correct under one rule.
    Child = aliased(WorkItem)
    child_advancing = (
        select(1)
        .where(
            Child.imported_project_id == project.id,
            Child.epic_id == WorkItem.id,
            func.upper(Child.status).notin_(not_started),
        )
        .correlate(WorkItem)
        .exists()
    )
    child_in_flight = (
        select(1)
        .where(
            Child.imported_project_id == project.id,
            Child.epic_id == WorkItem.id,
            func.upper(Child.status).notin_(not_started | finished),
        )
        .correlate(WorkItem)
        .exists()
    )
    feature_in_not_started = func.upper(WorkItem.status).in_(not_started)
    feature_touched = func.upper(WorkItem.status).notin_(not_started)
    feature_in_flight = func.upper(WorkItem.status).notin_(not_started | finished)

    # touched_expr: feature past TODO, OR feature still in TODO + a child past TODO.
    # in_flight_expr: feature in IN_PROGRESS-style state, OR feature in TODO + child in flight.
    # Crucially, when the feature is FINISHED (DONE/CLOSED/etc),
    # in_flight_expr evaluates to false regardless of children — that's
    # the "team explicitly signed off" semantic.
    touched_expr = or_(feature_touched, and_(feature_in_not_started, child_advancing))
    in_flight_expr = or_(feature_in_flight, and_(feature_in_not_started, child_in_flight))

    # Single query — for each phase, count touched items and in-flight
    # items separately. Lets us pick max-touched as current and
    # identify loose ends in one round trip.
    touched_q = await db.execute(
        select(
            ProjectPhase.slug,
            ProjectPhase.sort_order,
            func.sum(case((touched_expr, 1), else_=0)).label("touched"),
            func.sum(case((in_flight_expr, 1), else_=0)).label("in_flight"),
        )
        .join(WorkItem, WorkItem.phase_id == ProjectPhase.id)
        .where(
            ProjectPhase.project_id == project.id,
            WorkItem.imported_project_id == project.id,
        )
        .group_by(ProjectPhase.slug, ProjectPhase.sort_order)
    )
    rows = touched_q.all()

    # Max-touched = current phase.
    current_slug: Optional[str] = None
    current_order: int = -1
    for slug, order, touched, _ in rows:
        if touched and order > current_order:
            current_order = order
            current_slug = slug

    if current_slug is None:
        return None, []

    # Loose ends = phases earlier than current that still have in-flight items.
    also_active: list[str] = []
    for slug, order, _, in_flight in rows:
        if in_flight and order < current_order:
            also_active.append(slug)

    return current_slug, also_active


